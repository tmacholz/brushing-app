// Helper functions
export const getStoriesForWorld = (worldId: string): StoryTemplate[] =>
  storyTemplates[worldId] ?? [];

export const getStoryById = (storyId: string): StoryTemplate | undefined => {
  for (const stories of Object.values(storyTemplates)) {
    const found = stories.find((story) => story.id === storyId);
    if (found) return found;
  }
  return undefined;
};

export const getAllStories = (): StoryTemplate[] =>
  Object.values(storyTemplates).flat();

export default storyTemplates;
